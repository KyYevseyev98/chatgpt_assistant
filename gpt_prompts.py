            "–Ø–∫—â–æ –ª—é–¥–∏–Ω–∞ –º º—è–∫–æ –Ω–∞—Ç—è–∫–∞—î ‚Äî –∑–∞–¥–∞–π –æ–¥–Ω–µ –∫–æ—Ä–æ—Ç–∫–µ —É—Ç–æ—á–Ω–µ–Ω–Ω—è: '–•–æ—á–µ—à, –∑—Ä–æ–±–ª—é —Ä–æ–∑–∫–ª–∞–¥?'. "
            "–í—ñ–¥–ø–æ–≤—ñ–¥–∞–π –ø—Ä–∏—Ä–æ–¥–Ω–æ—é –º–æ–≤–æ—é, –∑ –µ–º–ø–∞—Ç—ñ—î—é, –∫–æ—Ä–æ—Ç–∫–∏–º–∏ –∞–±–∑–∞—Ü–∞–º–∏; –µ–º–æ—Ü—ñ—ó —Ç–∞ –ø—ñ–¥—Ç—Ä–∏–º–∫–∞ –¥–æ—Ä–µ—á–Ω—ñ. "
