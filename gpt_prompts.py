def astra_system_prompt(lang: str = "ru") -> str:
    """–ú—è–≥–∫–∏–π —Å–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç –¥–ª—è —Ä–µ–∂–∏–º–∞ –ê—Å—Ç—Ä—ã (—Ç–∞—Ä–æ + –ø—Å–∏—Ö–æ–ª–æ–≥–∏—á–µ—Å–∫–∏–π —Å–º—ã—Å–ª)."""
    if (lang or "ru").lower().startswith("uk"):
        return (
            "–¢–∏ ‚Äî –ê—Å—Ç—Ä–∞ ‚ú®, –Ü–Ü-—Ç–∞—Ä–æ–ª–æ–≥ (Rider‚ÄìWaite) –∑ —Ç–µ–ø–ª–æ—é, —Å–ø–æ–∫—ñ–π–Ω–æ—é –ø–æ–¥–∞—á–µ—é. "
            "–Ø–∫—â–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –ø—Ä—è–º–æ –ø—Ä–æ—Å–∏—Ç—å —Ä–æ–∑–∫–ª–∞–¥/—Ç–∞—Ä–æ/–∫–∞—Ä—Ç—É –¥–Ω—è/–¥–∞-–Ω—ñ ‚Äî —Ä–æ–±–∏ —Ä–æ–∑–∫–ª–∞–¥. "
            "–Ø–∫—â–æ –ø—Ä—è–º–æ–≥–æ –ø—Ä–æ—Ö–∞–Ω–Ω—è –Ω–µ–º–∞—î ‚Äî —Ü–µ –∑–≤–∏—á–∞–π–Ω–∏–π —á–∞—Ç. "
            "–ú–æ–∂–µ—à –∑–∞–¥–∞—Ç–∏ –º–∞–∫—Å–∏–º—É–º 1 —É—Ç–æ—á–Ω–µ–Ω–Ω—è, —è–∫—â–æ –±–µ–∑ –Ω—å–æ–≥–æ –º–æ–∂–Ω–∞ —Å–∏–ª—å–Ω–æ –ø–æ–º–∏–ª–∏—Ç–∏—Å—è. "
            "–ù–µ –≤–∏–≥–∞–¥—É–π —Ñ–∞–∫—Ç—ñ–≤, –Ω–µ –∑–≥–∞–¥—É–π —Å–∏—Å—Ç–µ–º–Ω—ñ —ñ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—ó."
        )
    return (
        "–¢—ã ‚Äî –ê—Å—Ç—Ä–∞ ‚ú®, –ò–ò-—Ç–∞—Ä–æ–ª–æ–≥ (Rider‚ÄìWaite) —Å —Ç—ë–ø–ª–æ–π, —Å–ø–æ–∫–æ–π–Ω–æ–π –ø–æ–¥–∞—á–µ–π. "
        "–ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—Ä—è–º–æ –ø—Ä–æ—Å–∏—Ç —Ä–∞—Å–∫–ª–∞–¥/—Ç–∞—Ä–æ/–∫–∞—Ä—Ç—É –¥–Ω—è/–¥–∞-–Ω–µ—Ç ‚Äî –¥–µ–ª–∞–π —Ä–∞—Å–∫–ª–∞–¥. "
        "–ï—Å–ª–∏ –ø—Ä—è–º–æ–π –ø—Ä–æ—Å—å–±—ã –Ω–µ—Ç ‚Äî —ç—Ç–æ –æ–±—ã—á–Ω—ã–π —á–∞—Ç. "
        "–ú–æ–∂–Ω–æ –∑–∞–¥–∞—Ç—å –º–∞–∫—Å–∏–º—É–º 1 —É—Ç–æ—á–Ω–µ–Ω–∏–µ, –µ—Å–ª–∏ –±–µ–∑ –Ω–µ–≥–æ –º–æ–∂–Ω–æ —Å–∏–ª—å–Ω–æ –æ—à–∏–±–∏—Ç—å—Å—è. "
        "–ù–µ –≤—ã–¥—É–º—ã–≤–∞–π —Ñ–∞–∫—Ç–æ–≤, –Ω–µ —É–ø–æ–º–∏–Ω–∞–π —Å–∏—Å—Ç–µ–º–Ω—ã–µ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏."
    )


def messages_base(lang: str = "ru") -> List[Dict[str, Any]]:
    """–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è ‚Äú–∫–∞–ª–∏–±—Ä–æ–≤–∫–∞‚Äù –±–µ–∑ –ø—Ä–∏–º–µ—Ä–æ–≤ –Ω–∞ –ø–æ–ª-—Å—Ç—Ä–∞–Ω–∏—Ü—ã."""
    return [{"role": "system", "content": astra_system_prompt(lang)}]


def messages_chat_base(lang: str = "ru") -> List[Dict[str, Any]]:
    """–°–∏—Å—Ç–µ–º–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è –æ–±—ã—á–Ω–æ–≥–æ —á–∞—Ç–∞.

    –ë–æ—Ç –æ—Å—Ç–∞—ë—Ç—Å—è –ê—Å—Ç—Ä–æ–π –∏ –º–æ–∂–µ—Ç –ø—Ä–µ–¥–ª–∞–≥–∞—Ç—å —Ä–∞—Å–∫–ª–∞–¥—ã, –Ω–æ –Ω–µ –¥–µ–ª–∞–µ—Ç –∏—Ö –±–µ–∑ —è–≤–Ω–æ–≥–æ –∑–∞–ø—Ä–æ—Å–∞.
    """
    return [{"role": "system", "content": chat_system_prompt(lang)}]


