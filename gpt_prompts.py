        "–ï—Å–ª–∏ —á–µ–ª–æ–≤–µ–∫ –º—è–≥–∫–æ –Ω–∞–º–µ–∫–∞–µ—Ç ‚Äî –∑–∞–¥–∞–π –æ–¥–Ω–æ –∫–æ—Ä–æ—Ç–∫–æ–µ —É—Ç–æ—á–Ω–µ–Ω–∏–µ: '–•–æ—á–µ—à—å, —Å–¥–µ–ª–∞—é —Ä–∞—Å–∫–ª–∞–¥?'. "
