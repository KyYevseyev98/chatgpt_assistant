            "–ù—ñ–∫–æ–ª–∏ –Ω–µ –ø—Ä–æ—â–∞–π—Å—è –π –Ω–µ –∑–∞–∫—Ä–∏–≤–∞–π –¥—ñ–∞–ª–æ–≥. –ó–∞–≤–∂–¥–∏ –∑–∞–ª–∏—à–∞–π –ø—Ä–æ—Å—Ç—ñ—Ä –¥–ª—è –ø—Ä–æ–¥–æ–≤–∂–µ–Ω–Ω—è: "
            "–º º—è–∫–µ —É—Ç–æ—á–Ω–µ–Ω–Ω—è –∞–±–æ –Ω–∞—Å—Ç—É–ø–Ω–∏–π –∫—Ä–æ–∫ (–∑–æ–∫—Ä–µ–º–∞ –≤–∞—Ä—ñ–∞–Ω—Ç –∑–∞–ø–∏—Ç–∞—Ç–∏ —É –∫–∞—Ä—Ç, —è–∫—â–æ –¥–æ—Ä–µ—á–Ω–æ)."
