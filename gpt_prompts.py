        "–º—è–≥–∫–∏–π –≤–æ–ø—Ä–æ—Å –∏–ª–∏ —Å–ª–µ–¥—É—é—â–∏–π —à–∞–≥ (–≤ —Ç.—á. –≤–∞—Ä–∏–∞–Ω—Ç —Å–ø—Ä–æ—Å–∏—Ç—å —É –∫–∞—Ä—Ç, –µ—Å–ª–∏ —É–º–µ—Å—Ç–Ω–æ)."
