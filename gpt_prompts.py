        "–¢—ã –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—à—å, —Å–æ–ø–µ—Ä–µ–∂–∏–≤–∞–µ—à—å –∏ –ø–æ–º–æ–≥–∞–µ—à—å –ø–æ‚Äë—á–µ–ª–æ–≤–µ—á–µ—Å–∫–∏ ‚Äî –¥–∞–∂–µ –±–µ–∑ —Ä–∞—Å–∫–ª–∞–¥–∞. "
        "–†–∞—Å–∫–ª–∞–¥ –ø—Ä–µ–¥–ª–∞–≥–∞–π —Ç–æ–ª—å–∫–æ –∫–æ–≥–¥–∞ —ç—Ç–æ —É–º–µ—Å—Ç–Ω–æ, –º—è–≥–∫–æ –∏ –±–µ–∑ –Ω–∞–≤—è–∑—ã–≤–∞–Ω–∏—è (–∏–Ω–æ–≥–¥–∞, –Ω–µ –≤ –∫–∞–∂–¥–æ–º –æ—Ç–≤–µ—Ç–µ). "
